<template>
  <div class="my-body">
    <common-alertdialog></common-alertdialog>
    <common-window-confirm></common-window-confirm>
    <div v-show="isLoading">
      <div class="loading">Loading&#8230;</div>
    </div>
    <div v-show="isLoading_opa">
      <div class="loading" style="opacity:0.5;">Loading&#8230;</div>
    </div>
    <router-view/>
  </div>
</template>

<script>
import constantUtil from "./common/constantUtil/index";
import uiUtil from "./common/uiUtil/index";

export default {
  name: "App",
  data() {
    return {
      isLoading: false,
      isLoading_opa: false,
      stackCall: 0
    };
  },
  created() {
    uiUtil.bus.on(constantUtil.EVENT.COMMMON.GLOBALLOADING, flag => {
      this.isLoading = true;
      if (flag != undefined) {
        this.stackCall += flag;
      } else {
        this.stackCall++;
      }
      console.log("GLOBALLOADING = ", this.stackCall);
    });
    uiUtil.bus.on(constantUtil.EVENT.COMMMON.GLOBALLOADED, flag => {
      this.stackCall--;
      if (this.stackCall == 0) {
        this.isLoading = false;
      }
      console.log("GLOBALLOADED = ", this.stackCall);
    });
    uiUtil.bus.on(constantUtil.EVENT.COMMMON.GLOBALLOADING_OPA, flag => {
      this.isLoading_opa = true;
    });
    uiUtil.bus.on(constantUtil.EVENT.COMMMON.GLOBALLOADED_OPA, flag => {
      this.isLoading_opa = false;
    });
  }
};
</script>

